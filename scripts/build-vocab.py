def main():
    out = []
    for grade, words in VOCAB.items():
        for word, meaning, example in words:
            out.append({
                "word": word,
                "grade": grade,
                "meaning": meaning,
                "example": example,
            })
    # shuffle deterministically so the order isn't alphabetical-by-grade
    import random
    rng = random.Random(42)
    rng.shuffle(out)

    os.makedirs("public/data", exist_ok=True)
    with open("public/data/vocabulary.json", "w") as f:
        json.dump(out, f, indent=2)
    print(f"Wrote {len(out)} words to public/data/vocabulary.json")


if __name__ == "__main__":
    main()
